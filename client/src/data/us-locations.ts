// Build final deduplicated city list, sorted by population desc, with region+metro attached
const _seen = new Set<string>();
export const US_CITIES_CLEAN: UsCity[] = US_CITIES.reduce<UsCity[]>((acc, city) => {
  if (!_seen.has(city.slug)) {
    _seen.add(city.slug);
    acc.push({
      ...city,
      region: REGION_MAP[city.stateCode] || "Other",
      metro: METRO_MAP[city.slug],
    });
  }
  return acc;
}, []).sort((a, b) => b.population - a.population);

export const getCitiesByState = (stateCode: string) =>
  US_CITIES_CLEAN.filter(c => c.stateCode === stateCode);

export const getCitiesByRegion = (region: string) =>
  US_CITIES_CLEAN.filter(c => c.region === region);

export const getCitiesByMetro = (metro: string) =>
  US_CITIES_CLEAN.filter(c => c.metro === metro);

export const getStateCodes = () =>
  Array.from(new Set(US_CITIES_CLEAN.map(c => c.stateCode))).sort();
